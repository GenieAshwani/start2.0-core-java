package operators;

public class OperatorP02 {

    public static void main(String[] args) {
        int a,b;
        a=12;
        b=10;

        System.out.println(a==b);
        System.out.println(a!=b);
        System.out.println(a>b);
        System.out.println(a<b);
        System.out.println(a>=b);
        System.out.println(a<=b);

        System.out.println("--------------------------------");
        boolean a1=false;
        boolean a2=false;
        System.out.println(a1&&a2);
        System.out.println(a1||a2);
        System.out.println(!(a1&&a2));


    }
}
